import math

# Функция для нахождения всех положительных делителей числа n.
def find_divisors(n):
    # Инициализируем пустой список, который будет содержать делители.
    divisors = []

    # Создаем цикл, который перебирает числа от 1 до корня из n.
    for i in range(1, int(math.sqrt(n)) + 1):
        # Проверяем, делится ли n на i без остатка.
        if n % i == 0:
            # Если да, добавляем i и соответствующий делитель в список.
            divisors.append(i)

            # Если число n делится на i, и результат не равен i, то добавляем его тоже.
            if n // i != i:
                divisors.append(n // i)

    # Сортируем список делителей по возрастанию.
    divisors.sort()

    # Возвращаем список делителей.
    return divisors

# Пример использования:
number = int(input("Введите число: "))
divisors = find_divisors(number)

# Выводим найденные делители.
print(divisors)
