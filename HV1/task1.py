# Найдите сумму цифр трехзначного числа n.

# Результат сохраните в перменную res.

# n = 123 -> res = 6 (1 + 2 + 3)

# n = 100 -> res = 1 (1 + 0 + 0)

n = int(input("Введите трехзначное число: "))
original_n = n  # Сохраняем оригинальное число для вывода результата

# Инициализируем переменную для суммы цифр
res = 0

# Используем цикл while для извлечения цифр и нахождения их суммы
while n > 0:
    digit = n % 10  # Извлекаем последнюю цифру
    res += digit    # Добавляем цифру к сумме
    n //= 10        # Удаляем последнюю цифру

# Выводим результат
print(f"Сумма цифр числа {original_n} равна {res}")

